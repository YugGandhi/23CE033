#include<stdio.h>
int main()
{
    float pr;
    printf("Enter Your Percentage -->");
    scanf("%d",&pr);

    if ( pr >= 90 )
    {
        printf("Distinction");

    }
     else if (pr >= 80 )
    {
        printf("First Class");
    }
    else if (pr >= 60 )
    {
        printf("SecondClass");
    }
    else if ( pr >= 45 )
    {
        printf("Pass Class");
    }
    else
    {
        printf("FAIL , Try Hard Next Time");
    }
    printf("Good Luck");
    return 0;

}











































































































































